$(document).ready(function(){
var iconElement = document.getElementById('icon');
var hamburger = $('#icon');
var htoggle = false;
var options = {
    from: 'fa-bars',
    to: 'fa-times-circle',
    animation: 'rubberBand'
};
var options2 = {
    from: 'fa-times-circle',
    to: 'fa-bars',
    animation: 'rubberBand'
};

hamburger.click(function () {
      hamburger_cross();      
});  
  
function hamburger_cross(){
if(htoggle==true){
  iconate(iconElement, options2);
  $('#text2').fadeOut();
  $('#menu_left').animate({left:'-215px'});
  $('#overlay').hide();
  $('#icon').animate({top:'398px'},200);
  $('#straight_a').animate({top:'450px'},700);
  $('#text1').fadeIn(2000);
  htoggle=false;
}else{
  $('#text1').fadeOut(10);
  $('#menu_left').animate({left:'10px'});
  $('#overlay').show();
  $('#icon').animate({top:'750px'},200);
  $('#straight_a').animate({top:'850px'},500);
  iconate(iconElement, options);
  $('#text2').fadeIn(1500);
  htoggle=true;
}    
}    
});
